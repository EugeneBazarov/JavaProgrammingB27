package day19_loops;

public class LoopBackwards {
    public static void main(String[] args) {


        for(int i = 50; i >= 0; i--){
            System.out.println(i + " ");

        }

        System.out.println();

        for(char letter = 'A'; letter <= 'Z'; letter++){
            System.out.println(letter + " ");

        }






    }
}
