package day03_variables;

public class DateTomorrow {

    public static void main(String[] args) {

        int month = 6;
        int day = 20;
        int year = 2022;
        System.out.println("Today is " + month + "\\" + day + "\\" + year);

        day = 21;

        System.out.println("Tomorrow is " + month + "\\" + day + "\\" + year);

    }
}
